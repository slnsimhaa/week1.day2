package week1.day2;

public class Calculator {
	public int addThreeNumbers(int a, int b, int c) {
		return a + b + c;
	}

	public int subTwoNumbers(int a, int b) {
		return a - b;
	}
	
	public double mulTwoNumbers(double a, double b) {
		return a * b;
	}
	
	public float divTwoNumbers(float a, float b) {
		return a / b;
	}
}

